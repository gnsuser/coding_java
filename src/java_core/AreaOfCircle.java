package java_core;

public class AreaOfCircle {
public static void main(String args[]) {
	float pi = 3.14f;
	int radius = 10;
	float area = pi*(radius*radius);
	System.out.println(area);
	
	//below code is to covert fahrenheit to celsius
	int f = 50;
	float c = ((f-32)/9)*5f;
	System.out.println(c);
}
}

